"""Export experiment figures and tables to paper directory with IEEE Access naming."""

from __future__ import annotations

import shutil
from pathlib import Path

import pandas as pd

PROJECT_ROOT = Path(__file__).resolve().parents[3]
EXP_ROOT = PROJECT_ROOT / "instrument_cls_experiments"
FIGURES_DIR = EXP_ROOT / "reports/figures"
TABLES_DIR = EXP_ROOT / "reports/tables"

PAPER_DIR = PROJECT_ROOT / "paper/ACCESS_latex_template_20240429"

# author prefix
AUTHOR_PREFIX = "liu"

# figure map: paper number → experiment output filename
# Fig. 1-4 are manually created; only mapping generated ones here
FIGURE_MAP = {
    5: "exp03_learning_curve_test_macro_f1.pdf",
    6: "exp03_learning_curve_external_test_macro_f1.pdf",
    7: "exp04_layer_probe_macro_f1.pdf",
    8: "exp04_umap_seed1_test.pdf",
    9: "exp04_confusion_mert_full_ft_external_test.pdf",
}

# table map: paper number → experiment output filename
# Table I-IV are hand-written in LaTeX; only mapping data tables here
TABLE_MAP = {
    5: "exp01_transfer_summary_mean_std.csv",
    # 6: merged table, generated by this script
    7: "exp02_adaptation_summary_mean_std.csv",
    8: "exp02_adaptation_cost_summary_mean_std.csv",
    9: "exp03_data_efficiency_summary_mean_std.csv",
    10: "exp04_layer_probe_table.csv",
    11: "exp04_error_case_summary.csv",
}


def build_table_vi() -> Path:
    """Merge exp01 + exp02 summaries into Table VI (all-methods overview)."""
    exp01 = pd.read_csv(TABLES_DIR / "exp01_transfer_summary_mean_std.csv")
    exp02 = pd.read_csv(TABLES_DIR / "exp02_adaptation_summary_mean_std.csv")

    combined = pd.concat([exp01, exp02], ignore_index=True)

    # dedup: MERT linear probe appears in both files; keep exp01 version
    combined = combined.drop_duplicates(
        subset=["method", "split"], keep="first",
    )

    # keep only test and external_test
    combined = combined[combined["split"].isin(["test", "external_test"])]

    # select key columns
    cols = [
        "method", "model", "split",
        "accuracy_mean", "accuracy_std",
        "macro_f1_mean", "macro_f1_std",
        "macro_precision_mean", "macro_precision_std",
        "macro_recall_mean", "macro_recall_std",
    ]
    combined = combined[cols].sort_values(["split", "method"]).reset_index(drop=True)

    output_path = TABLES_DIR / "table_vi_all_methods_overview.csv"
    combined.to_csv(output_path, index=False)
    print(f"  generated Table VI merged data: {output_path.name}")
    return output_path


def main() -> None:
    print("export_paper_assets: exporting figures and tables to paper directory")
    print("=" * 60)

    PAPER_DIR.mkdir(parents=True, exist_ok=True)

    # export figures
    print("\n[Figures]")
    for fig_num, src_name in FIGURE_MAP.items():
        src = FIGURES_DIR / src_name
        dst = PAPER_DIR / f"{AUTHOR_PREFIX}{fig_num}.pdf"
        if not src.exists():
            print(f"  SKIP Fig. {fig_num}: {src_name} not found (run plot script first)")
            continue
        shutil.copy2(src, dst)
        print(f"  Fig. {fig_num}: {src_name} → {dst.name}")

    # generate Table VI
    print("\n[Table VI — merged]")
    table_vi_path = build_table_vi()

    # export tables
    print("\n[Tables]")
    # add Table VI
    full_table_map = dict(TABLE_MAP)
    full_table_map[6] = table_vi_path.name

    for tbl_num in sorted(full_table_map.keys()):
        src_name = full_table_map[tbl_num]
        src = TABLES_DIR / src_name
        dst = PAPER_DIR / f"{AUTHOR_PREFIX}.t{tbl_num}.csv"
        if not src.exists():
            print(f"  SKIP Table {tbl_num}: {src_name} not found")
            continue
        shutil.copy2(src, dst)
        print(f"  Table {tbl_num}: {src_name} → {dst.name}")

    # report missing assets
    print("\n" + "=" * 60)
    print("[Missing figures — manually created]")
    missing_figs = {
        1: "Data/experiment pipeline diagram",
        2: "Exp 1 test vs external_test bar chart",
        3: "Performance vs params scatter",
        4: "Performance vs time scatter",
    }
    for fig_num, desc in missing_figs.items():
        print(f"  Fig. {fig_num} ({AUTHOR_PREFIX}{fig_num}.pdf): {desc}")

    print("\n[Missing tables — hand-written in LaTeX]")
    manual_tables = {
        1: "Data sources and task setup",
        2: "Frozen classes and data statistics",
        3: "Method matrix",
        4: "Experiment matrix",
    }
    for tbl_num, desc in manual_tables.items():
        print(f"  Table {tbl_num}: {desc}")

    print("\nexport_paper_assets: done")


if __name__ == "__main__":
    main()
